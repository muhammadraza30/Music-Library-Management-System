    private void renderSongsOnTable(ArrayList<SongsData> songsArray) {
        //DefaultTableModel is used for storing data in cells    

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        //Setting Row count to 0
        model.setRowCount(0);
        
        //using For Each Loop which is Going through songsArray and check's it in the SongsData class
        for (SongsData song : songsArray) {
        //Now making the obj of Object class rowData[] and making it an array so it can easily stores data in the cells or rows
            Object[] row = {song.getId(), song.getSongName(), song.getArtistName(),
                song.getGenre(), song.getYear()};

            model.addRow(row);
        }
    }

