    //This final method render songs categories in the box
    public final void renderSongsCategoriesOnFilterDropDown() {
        //create obj of list for getting the songs category from SongsCategory
        List<String> categories = SongsCategory.getAllSongsCategory();
        //we add items in combo box mood and all which shows all the songs on these item
        comboBox.addItem("Mood...");

        for (String category : categories) {
            comboBox.addItem(category);
        }

        comboBox.addItem("All");
    }

    //This method is performing Functionality by filtering it with mood
