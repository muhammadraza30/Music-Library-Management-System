    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        //keypress event 
        if (jTextField1.getText() != null && !jTextField1.getText().isEmpty()) {
            //Here we creating an ArrayList<SongsData> obj which is equals to SongsData and from it we call performSearch func =>
            ArrayList<SongsData> searchedSong = new SongsData().performSearch(h.getSongs(), jTextField1.getText(), false);
            //DefaultTableModel is used for storing data in cells    

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            //Setting Row count to 0
            model.setRowCount(0);

            //Render's data on table
            renderSongsOnTable(searchedSong);

        } else {
            //Render's data on table (By Default)
            renderSongsOnTable(new SongsFileHandling().getSongs());
        }
    }//GEN-LAST:event_jTextField1KeyReleased

