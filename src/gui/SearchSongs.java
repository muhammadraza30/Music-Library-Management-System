    //rendering the songs on the table 
    private void renderSongsOnTable(ArrayList<SongsData> songs) {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);

        for (SongsData song : songs) {
            Object[] row = {song.getId(), song.getSongName(), song.getArtistName(),
                song.getGenre(), song.getYear()};

            model.addRow(row);
        }
    }

